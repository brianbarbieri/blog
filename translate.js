let currentLang = 'en';

function updateLanguage() {
    // Sidebar & navigation
    document.getElementById('dropdown-btn-rooms').innerHTML = `<i class="fa fa-house"></i> ${translations[currentLang].rooms}`;
    // document.getElementById('bookLink').innerHTML = `<i class="fa fa-book"></i> ${translations[currentLang].book}`;
    document.getElementById('gardenLink').innerHTML = `<i class="fa fa-seedling"></i> ${translations[currentLang].garden}`;
    document.getElementById('surroundingsLink').innerHTML = `<i class="fa fa-archway"></i> ${translations[currentLang].surroundings}`;
    document.getElementById('contactLink').innerHTML = `<i class="fa fa-envelope"></i> ${translations[currentLang].contact}`;

    // Main content
    document.getElementById('welcomeText').innerText = translations[currentLang].welcomeText;
    document.getElementById('introText').innerText = translations[currentLang].introText;

    document.getElementById('facilitiesTitle').innerText = translations[currentLang].facilitiesTitle;
    document.getElementById('facilitiesText').innerText = translations[currentLang].facilitiesText;

    document.getElementById('breakfastTitle').innerText = translations[currentLang].breakfastTitle;
    document.getElementById('breakfastText').innerText = translations[currentLang].breakfastText;

    document.querySelectorAll('.availDates').forEach(el => el.innerText = translations[currentLang].availDates);
    document.querySelectorAll('.checkIn').forEach(el => el.innerText = translations[currentLang].checkIn);
    document.querySelectorAll('.checkOut').forEach(el => el.innerText = translations[currentLang].checkOut);

    document.getElementById('olivoInfoText').innerText = translations[currentLang].olivoInfoText;
    document.getElementById('melanzaInfoText').innerText = translations[currentLang].melanzaInfoText;
    document.getElementById('cavaloInfoText').innerText = translations[currentLang].cavaloInfoText;

    document.getElementById('gardenTitle').innerText = translations[currentLang].gardenTitle;
    document.getElementById('gardenText').innerText = translations[currentLang].gardenText;

    document.getElementById('surroundTitle').innerText = translations[currentLang].surroundTitle;
    document.getElementById('acquiText').innerText = translations[currentLang].acquiText;
    document.getElementById('wineriesTitle').innerText = translations[currentLang].wineriesTitle;
    document.getElementById('wineriesText').innerText = translations[currentLang].wineriesText;
    document.getElementById('hikingTitle').innerText = translations[currentLang].hikingTitle;
    document.getElementById('hikingText').innerText = translations[currentLang].hikingText;
    document.getElementById('coolPlacesTitle').innerText = translations[currentLang].coolPlacesTitle;
    document.getElementById('coolPlacesText').innerText = translations[currentLang].coolPlacesText;

    document.getElementById('phoneLabel').innerText = translations[currentLang].phoneLabel;
    document.querySelectorAll('.bedroom').forEach(el => el.innerText = translations[currentLang].bedroom);
    document.querySelectorAll('.toilet').forEach(el => el.innerText = translations[currentLang].toilet);
    document.querySelectorAll('.shower').forEach(el => el.innerText = translations[currentLang].shower);
    document.querySelectorAll('.amenities').forEach(el => el.innerText = translations[currentLang].amenities);
    for (let i = 1; i <= 7; i++) {
        document.querySelectorAll(`.amen${i}`).forEach(el => el.innerText = translations[currentLang][`amen${i}`]);
    }
    for (let i = 1; i <= 9; i++) {
        document.getElementById(`rules${i}`).innerText = translations[currentLang][`rules${i}`];
    }
}

document.getElementById('langBtn').addEventListener('click', () => {
    currentLang = currentLang === 'en' ? 'nl' : 'en';
    console.log(currentLang);
    if (currentLang === 'nl') {
        langBtn.src = 'https://flagcdn.com/h20/gb.webp'; // English flag
        langBtn.alt = 'English';
    } else {
        langBtn.src = 'https://flagcdn.com/h20/nl.webp'; // Dutch flag
        langBtn.alt = 'Nederlands';
    }
    updateLanguage();
});

// initialize
updateLanguage();